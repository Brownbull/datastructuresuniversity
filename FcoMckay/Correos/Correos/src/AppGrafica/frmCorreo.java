    public void RescataCorreos()
    {
        try{
            FileReader f = new FileReader("entrada.txt");
            Scanner sf =  new Scanner(f);
            while(sf.hasNextLine()){
                String linea = sf.nextLine();                
                Correo cor = new Correo(linea);
                correos.push(cor);
            }
            sf.close();
            f.close();
        }
        catch(IOException e)
        {
            JOptionPane.showMessageDialog(this,"Error al abrir archivo entrada.txt");
        }
    }

